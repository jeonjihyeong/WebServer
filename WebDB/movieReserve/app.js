const app = express();
const server = http.createServer(app);

app.get('/:num',(req,res,next)=>{
    fs.readFile('HTMLPAGE.html',(err,data)=>{
        res.send(data.toString());
    })
})

app.get('/seats/:num',(req,res,next)=>{
    console.log(req.params.num)
    const index = req.params.num
    res.send(seats[index]);
});

server.listen(52273,()=>{
    console.log('Server Running at http://127.0.0.1:52273');
});

const io = socketIo(server);

io.sockets.on('connection',(socket)=>{
    socket.on('reserve',(data)=>{
        console.log("socket on!")
        const index =data.index
        const indexNum = index.substr(1,)
        seats[indexNum][data.y][data.x]= 2;
        io.sockets.emit('reserve',data);
    })
    socket.on('cancle',(data)=>{
        console.log("socket on!")
        const index =data.index
        const indexNum = index.substr(1,)
        seats[indexNum][data.y][data.x] = 1;
        io.sockets.emit('cancle',data)
    })
})